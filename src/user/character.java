package user;

public class character {
    //
    protected String name;
    protected String job;

    //name 변수를 return
    public String getName() {
        return name;
    }

    //name 변수를 할당
    public void setName(String name) {
        this.name = name;
    }

    //job 변수를 return
    public String getJob() {
        return job;
    }

    //job 변수를
    public void setJob(String job) {
        this.job = job;
    }
}
